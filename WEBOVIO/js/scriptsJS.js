/// ДОБАВДЕНИЯ КЛАССА АКТИВ ДЛЯ БУРГЕРА
$('.header__burger').click(function (event) {
	$(this).toggleClass('active');
	$('.burger__menu').toggleClass('active');
	$('.burger__link').toggleClass('active');

});

